        console.log('üé® –£–ª—É—á—à–∞–µ–º —Ç–µ–∫—Å—Ç –∫—Ä–∞—Å–∏–≤—ã–º–∏ —Å–∏–Ω–æ–Ω–∏–º–∞–º–∏...');
