    getRandomSynonym(word) {
        let synonyms = this.synonyms[word];
        if (!synonyms || synonyms.length === 0) return word;
        
        let randomIndex = Math.floor(Math.random() * synonyms.length);
        return synonyms[randomIndex];
    }

